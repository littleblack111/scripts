def statusRaw() -> status_t:
    return status_t[getStatusFd().read().strip()]

def statusBool() -> bool:
    return statusRaw() == status_t.ON or statusRaw() == status_t.TURNING_ON

def status() -> str:
    return statusRaw().value

def toggle():
    if statusRaw() == status_t.ON or statusRaw() == status_t.TURNING_ON:
        off()
    else: # status() == status_t.OFF or status() == status_t.TURNING_OFF:
        on()
    reloadWaybar()

